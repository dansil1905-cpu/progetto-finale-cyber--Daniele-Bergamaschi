    {
        return view('posts.create');
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|string|max:255',
            'content' => 'required|string',
        ]);

        Post::create([
            'title' => $request->title,
            'slug' => Str::slug($request->title) . '-' . uniqid(),
            'content' => $request->content,
            'status' => 'pending', // L'articolo parte in attesa di revisione
            'id_utente' => Auth::id(),
        ]);

        return redirect()->route('posts.index')->with('success', 'Articolo inviato con successo e in attesa di revisione!');
    }

